// What is a server?
// A server is a computer that provides data to other computers. It may serve data to systems on a local area network (LAN) or a wide area network (WAN) over the internet.
// A server can be a physical computer (hardware) or a software program (software).
// A server receives requests from other computers (clients) and sends back the requested data.
// A server shows pages and runs applications through the internet on browsers.

// What is a client?
// A client is a computer that requests data from a server. A client can be a physical computer (hardware) or a software program (software).
// A client sends requests to a server in order to get the data or the service.
// A client displays the data that the server returns.

// What is a web server?
// A web server is a computer that runs websites. It's a computer program that distributes web pages as they are requisitioned. The basic objective of the web server is to store, process and deliver web pages to the users. This intercommunication is done using Hypertext Transfer Protocol (HTTP).

